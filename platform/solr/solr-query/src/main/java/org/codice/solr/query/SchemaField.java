package org.codice.solr.query;

import org.apache.commons.lang.builder.ToStringBuilder;

public class SchemaField {

  private String name;

  private String specialSuffix;

  private String type;

  public SchemaField(String name, String type) {
    this.name = name;
    this.type = type;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }

  public String getSpecialSuffix() {
    return specialSuffix;
  }

  public void setSpecialSuffix(String specialSuffix) {
    this.specialSuffix = specialSuffix;
  }

  public String getType() {
    return type;
  }

  public void setType(String type) {
    this.type = type;
  }

  @Override
  public String toString() {
    return ToStringBuilder.reflectionToString(this);
  }
}
